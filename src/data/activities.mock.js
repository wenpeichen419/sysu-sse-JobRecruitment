// 小工具：把 YYYY-MM-DD 拆成 {year, month, day}
export function splitYMD(isoDate) {
  const [y, m, d] = (isoDate || '').split('-').map(n => parseInt(n, 10))
  return {
    year: y || '',
    month: String(m || '').padStart(2, '0'),
    day: String(d || '').padStart(2, '0')
  }
}
